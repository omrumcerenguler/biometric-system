export function normalizeReasonCode(reason) {
  if (!reason) return "SYS_UNKNOWN_ERROR";
  if (CANONICAL.includes(reason)) return reason;
  if (LEGACY_TO_CANONICAL[reason]) return LEGACY_TO_CANONICAL[reason];
  const up = String(reason).toUpperCase();
  if (LEGACY_TO_CANONICAL[up]) return LEGACY_TO_CANONICAL[up];
  return "SYS_UNKNOWN_ERROR";
}
